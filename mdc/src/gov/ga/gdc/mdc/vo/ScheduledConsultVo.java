package gov.ga.gdc.mdc.vo;

public class ScheduledConsultVo {
    
    
    private String uno;
    private String offenderName;
    private String reviewDecisionCode  ;
    private String reviewDecisionDesc;
    private String umReqConsultationCode ;
    private String umReqConsultationDesc ;
    private String apptTime;
    
    public String getUno() {
        return uno;
    }
    public void setUno(String uno) {
        this.uno = uno;
    }
    public String getOffenderName() {
        return offenderName;
    }
    public void setOffenderName(String offenderName) {
        this.offenderName = offenderName;
    }
    public String getReviewDecisionCode() {
        return reviewDecisionCode;
    }
    public void setReviewDecisionCode(String reviewDecisionCode) {
        this.reviewDecisionCode = reviewDecisionCode;
    }
    public String getReviewDecisionDesc() {
        return reviewDecisionDesc;
    }
    public void setReviewDecisionDesc(String reviewDecisionDesc) {
        this.reviewDecisionDesc = reviewDecisionDesc;
    }
    public String getUmReqConsultationCode() {
        return umReqConsultationCode;
    }
    public void setUmReqConsultationCode(String umReqConsultationCode) {
        this.umReqConsultationCode = umReqConsultationCode;
    }
    public String getUmReqConsultationDesc() {
        return umReqConsultationDesc;
    }
    public void setUmReqConsultationDesc(String umReqConsultationDesc) {
        this.umReqConsultationDesc = umReqConsultationDesc;
    }
    public String getApptTime() {
        return apptTime;
    }
    public void setApptTime(String apptTime) {
        this.apptTime = apptTime;
    }

    

}
